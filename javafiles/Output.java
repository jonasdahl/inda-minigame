/**
 * A class that handles output.
 * 
 * @author Jonas Dahl
 * @version 2013.12.11
 */
public class Output {
    /**
     * Prints the information to the terminal, using System.out.println() atm.
     */
    public static void println(String printString) {
        System.out.println(printString);
    }
    
    /**
     * Prints the information to the terminal, using System.out.println() atm.
     */
    public static void println() {
        System.out.println();
    }
    
    /**
     * Prints the information to the terminal, using System.out.print() atm.
     */
    public static void print(String printString) {
        System.out.print(printString);
    }
}
